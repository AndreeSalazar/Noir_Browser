from pathlib import Path
import shutil

class WebGPUGenerator:
    def __init__(self, output_dir: str = "src/webgpu"):
        self.output_dir = Path(output_dir)
        if self.output_dir.exists():
            shutil.rmtree(self.output_dir)
        self.output_dir.mkdir(parents=True, exist_ok=True)

    def generate_all(self):
        self.generate_mod()
        self.generate_device()
        self.generate_pipeline()
        self.generate_shaders()
        self.generate_buffer()
        self.generate_texture()
        self.generate_renderer()
        self.generate_bridge()
        self.generate_compute()
        self.generate_pwa()
        print()
        print("=" * 60)
        print("  WebGPU Module for Noir Browser - GENERATED")
        print("  Multi-backend: Vulkan/Metal/DX12")
        print("=" * 60)

