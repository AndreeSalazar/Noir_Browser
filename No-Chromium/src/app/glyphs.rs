//! Glyphs - Bitmap font para rendering de texto
//!
//! Define los glyphs bitmap de 7x12 pixels para caracteres básicos.

/// Dibuja un glifo individual
pub fn draw_glyph(
    buf: &mut [u32],
    stride: usize,
    x: i32,
    y: i32,
    ch: char,
    color: u32,
    _scale: f32,
    char_w: i32,
    char_h: i32,
) {
    let pattern = get_glyph(ch);
    for row in 0..char_h {
        let py = y + row;
        if py < 0 { continue; }
        let bits = pattern[row as usize];
        for col in 0..char_w {
            let px = x + col;
            if px < 0 { continue; }
            if bits & (1 << (char_w - 1 - col)) != 0 {
                let idx = (py as usize) * stride + px as usize;
                if idx < buf.len() {
                    buf[idx] = color;
                }
            }
        }
    }
}

